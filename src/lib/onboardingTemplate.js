// ─── Quiz Lookup Helper ──────────────────────────────────────────
export function getQuizForStage(templateStageId) {
  if (templateStageId === "summary") {
    return ONBOARDING_TEMPLATE.summary_quiz;
  }
  const stage = ONBOARDING_TEMPLATE.stages.find((s) => s.template_stage_id === templateStageId);
  return stage?.quiz;
}